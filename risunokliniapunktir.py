"""пунктир рисунок"""
from turtle import *
t = Turtle()
t.screen.setup(800, 800) # рисунок квадрата экрана
for i in range(10): # цикл 
    t.fd(8) # линия 8 пикселей 
    t.up() # пустое пространство - движение черепашки
    t.fd(8) # линия 8 пикселей  
    t.down() #  взврат к рисунку
t.screen.exitonclick() # реагирование  н анажатие кнопки мыши
t.screen.mainloop() # остановка программы
